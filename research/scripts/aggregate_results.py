"""Aggregate all results/*/summary.json into paper-ready CSV + LaTeX tables.

Outputs (under paper-emnlp/figures/):
    table_1_benchmark.tex           composition of ToolGuard-Bench
    table_2_per_distribution.tex    block-rate per defender per distribution
    table_3_overall.tex             headline block-rate / FP / latency / cost
    pareto_data.csv                 cost-vs-block-rate scatter inputs
    summary_index.csv               flat index of every run

Each table is autogenerated and clearly marked. Numbers come straight from
results/*/summary.json — no manual transcription.
"""

from __future__ import annotations

import argparse
import csv
import json
import re
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
RESULTS = ROOT / "results"
OUT_DIR = ROOT / "paper-emnlp" / "figures"


def _load_summaries() -> list[dict]:
    """Load every summary.json, then keep only the most recent run per
    (defender, benchmark) so re-runs with the same config don't pollute the
    aggregate tables."""
    raw = []
    for run_dir in sorted(RESULTS.iterdir()):
        s_path = run_dir / "summary.json"
        if not s_path.exists():
            continue
        s = json.loads(s_path.read_text())
        s["_run_dir"] = run_dir.name
        raw.append(s)
    latest: dict[tuple, dict] = {}
    for s in raw:
        key = (
            _baseline_label(s),
            s.get("benchmark", ""),
            s.get("model", ""),
        )
        if key not in latest or s.get("timestamp", "") > latest[key].get("timestamp", ""):
            latest[key] = s
    return list(latest.values())


def _bench_path() -> Path:
    return ROOT / "benchmark" / "data" / "aegis-bench.jsonl"


def write_table_1_benchmark(out: Path) -> None:
    """Composition of ToolGuard-Bench by source / partition / label."""
    bp = _bench_path()
    if not bp.exists():
        print(f"  [table 1] skip — {bp} missing")
        return
    from collections import Counter
    by_src: Counter = Counter()
    by_src_label: Counter = Counter()
    by_src_dist: Counter = Counter()
    by_src_gran: Counter = Counter()
    with bp.open() as f:
        for line in f:
            r = json.loads(line)
            s = r["source"]
            by_src[s] += 1
            by_src_label[(s, r["label"])] += 1
            by_src_dist[(s, r["distribution"])] += 1
            by_src_gran[(s, r["granularity"])] += 1

    lines = [
        "% Auto-generated by scripts/aggregate_results.py — DO NOT EDIT BY HAND",
        r"\begin{table}[t]",
        r"\centering\small",
        r"\caption{Composition of \bench. Each source is partitioned by label, "
        r"distribution shift relative to the AEGIS rule set, and tool API "
        r"granularity.}",
        r"\label{tab:bench}",
        r"\begin{tabular}{lrrrrrrr}",
        r"\toprule",
        r"Source & Total & Mal. & Ben. & In-D & Near-OOD & Far-OOD & Fine \\",
        r"\midrule",
    ]
    for src, n in by_src.most_common():
        mal = by_src_label[(src, "malicious")]
        ben = by_src_label[(src, "benign")]
        ind = by_src_dist[(src, "in_distribution")]
        nood = by_src_dist[(src, "near_ood")]
        food = by_src_dist[(src, "far_ood")]
        fine = by_src_gran[(src, "fine")]
        src_safe = src.replace("_", "\\_")
        lines.append(
            f"{src_safe} & {n} & {mal} & {ben} & "
            f"{ind} & {nood} & {food} & {fine} \\\\"
        )
    total = sum(by_src.values())
    mal_t = sum(v for (s, l), v in by_src_label.items() if l == "malicious")
    ben_t = total - mal_t
    lines += [
        r"\midrule",
        f"\\textbf{{Total}} & \\textbf{{{total}}} & \\textbf{{{mal_t}}} & "
        f"\\textbf{{{ben_t}}} & & & & \\\\",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{table}",
    ]
    out.write_text("\n".join(lines) + "\n")
    print(f"  [table 1] wrote {out}")


def _is_obfuscated(s: dict) -> bool:
    return "obfuscated" in (s.get("benchmark") or "")


def _baseline_label(s: dict) -> str:
    if s.get("method") == "cascade":
        f = []
        if s.get("use_l1"): f.append("L1")
        if s.get("use_l2"): f.append("L2")
        if s.get("use_l3"): f.append("L3")
        return f"AEGIS-{'+'.join(f)}"
    name = s.get("baseline", "?")
    return {
        "no_defense": "No defense",
        "keyword_blacklist": "Keyword blacklist",
        "aegis_rules": "AEGIS-rules (L1)",
        "llm_judge_openai": f"LLM-judge {s.get('model', 'gpt-4o')}",
        "llm_judge_anthropic": f"LLM-judge {s.get('model', 'claude')}",
        "llama_guard": "Llama-Guard-3",
    }.get(name, name)


def write_table_3_overall(summaries: list[dict], out: Path) -> None:
    rows = [s for s in summaries if not _is_obfuscated(s)]
    rows.sort(key=lambda s: (-s.get("block_rate", 0.0), s.get("fp_rate", 0.0)))
    lines = [
        "% Auto-generated by scripts/aggregate_results.py",
        r"\begin{table}[t]",
        r"\centering\small",
        r"\caption{Headline results on \bench. Block rate is recall on "
        r"malicious tool calls; FP rate is on benign. Latency is end-to-end "
        r"per call; cost is total over the full benchmark.}",
        r"\label{tab:overall}",
        r"\begin{tabular}{lrrrrr}",
        r"\toprule",
        r"Defender & Block & FP & P50\,ms & P95\,ms & \$/run \\",
        r"\midrule",
    ]
    for s in rows:
        lat = s.get("latency_ms") or {}
        lines.append(
            f"{_baseline_label(s)} & "
            f"{(s['block_rate']*100):.1f} & "
            f"{(s['fp_rate']*100):.2f} & "
            f"{(lat.get('p50') or 0):.2f} & "
            f"{(lat.get('p95') or 0):.2f} & "
            f"{s.get('total_cost_usd', 0):.3f} \\\\"
        )
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    out.write_text("\n".join(lines) + "\n")
    print(f"  [table 3] wrote {out}")


def write_table_2_per_distribution(summaries: list[dict], out: Path) -> None:
    rows = [s for s in summaries if not _is_obfuscated(s)]
    rows.sort(key=lambda s: -s.get("block_rate", 0.0))
    lines = [
        "% Auto-generated by scripts/aggregate_results.py",
        r"\begin{table}[t]",
        r"\centering\small",
        r"\caption{Block-rate per defender per distribution partition. "
        r"Generalization gap = In-Dist $-$ Far-OOD.}",
        r"\label{tab:perdist}",
        r"\begin{tabular}{lrrrr}",
        r"\toprule",
        r"Defender & In-Dist & Near-OOD & Far-OOD & $\Delta$ \\",
        r"\midrule",
    ]
    for s in rows:
        d = s.get("by_distribution", {})
        ind = (d.get("in_distribution") or {}).get("block_rate")
        nood = (d.get("near_ood") or {}).get("block_rate")
        food = (d.get("far_ood") or {}).get("block_rate")
        gap = (ind - food) if (ind is not None and food is not None) else None
        def fmt(x):
            return f"{x*100:.1f}" if x is not None else "--"
        gap_s = fmt(gap) if gap is not None else "--"
        lines.append(
            f"{_baseline_label(s)} & {fmt(ind)} & {fmt(nood)} & "
            f"{fmt(food)} & {gap_s} \\\\"
        )
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    out.write_text("\n".join(lines) + "\n")
    print(f"  [table 2] wrote {out}")


def write_obfuscation_impact(summaries: list[dict], out: Path) -> None:
    """Per-defender block-rate on clean vs obfuscated benchmark."""
    pairs: dict[str, dict[str, float]] = {}
    for s in summaries:
        key = _baseline_label(s)
        slot = "obf" if _is_obfuscated(s) else "clean"
        pairs.setdefault(key, {})[slot] = s.get("block_rate", 0.0)
    lines = [
        "% Auto-generated by scripts/aggregate_results.py",
        r"\begin{table}[t]",
        r"\centering\small",
        r"\caption{Robustness to deterministic obfuscation transforms. "
        r"Decay = clean $-$ obfuscated; lower-is-better.}",
        r"\label{tab:obf}",
        r"\begin{tabular}{lrrr}",
        r"\toprule",
        r"Defender & Clean & Obfuscated & Decay \\",
        r"\midrule",
    ]
    for k, v in sorted(pairs.items()):
        c = v.get("clean")
        o = v.get("obf")
        if c is None or o is None:
            continue
        lines.append(
            f"{k} & {c*100:.1f} & {o*100:.1f} & {(c-o)*100:+.1f} \\\\"
        )
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    out.write_text("\n".join(lines) + "\n")
    print(f"  [obf table] wrote {out}")


def write_pareto_csv(summaries: list[dict], out: Path) -> None:
    rows = []
    for s in summaries:
        if _is_obfuscated(s):
            continue
        lat = s.get("latency_ms") or {}
        rows.append({
            "defender": _baseline_label(s),
            "block_rate": s.get("block_rate", 0.0),
            "fp_rate": s.get("fp_rate", 0.0),
            "p50_ms": lat.get("p50") or 0,
            "p95_ms": lat.get("p95") or 0,
            "cost_usd": s.get("total_cost_usd") or 0,
            "n": s.get("n_records") or s.get("n_test") or 0,
            "is_cascade": s.get("method") == "cascade",
        })
    if not rows:
        return
    with out.open("w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=rows[0].keys())
        w.writeheader()
        w.writerows(rows)
    print(f"  [pareto] wrote {out}")


def write_summary_index(summaries: list[dict], out: Path) -> None:
    rows = []
    for s in summaries:
        rows.append({
            "run": s.get("_run_dir"),
            "defender": _baseline_label(s),
            "benchmark": s.get("benchmark", ""),
            "n_records": s.get("n_records") or s.get("n_test") or 0,
            "block_rate": s.get("block_rate", 0.0),
            "fp_rate": s.get("fp_rate", 0.0),
            "p50_ms": (s.get("latency_ms") or {}).get("p50"),
            "cost_usd": s.get("total_cost_usd", 0.0),
            "wall_time_s": s.get("wall_time_s"),
        })
    if not rows:
        return
    with out.open("w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=rows[0].keys())
        w.writeheader()
        w.writerows(rows)
    print(f"  [index] wrote {out}")


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--out-dir", type=Path, default=OUT_DIR)
    args = p.parse_args()
    args.out_dir.mkdir(parents=True, exist_ok=True)

    summaries = _load_summaries()
    print(f"Loaded {len(summaries)} run summaries from {RESULTS}")

    write_table_1_benchmark(args.out_dir / "table_1_benchmark.tex")
    write_table_2_per_distribution(summaries, args.out_dir / "table_2_per_distribution.tex")
    write_table_3_overall(summaries, args.out_dir / "table_3_overall.tex")
    write_obfuscation_impact(summaries, args.out_dir / "table_4_obfuscation.tex")
    write_pareto_csv(summaries, args.out_dir / "pareto_data.csv")
    write_summary_index(summaries, args.out_dir / "summary_index.csv")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
